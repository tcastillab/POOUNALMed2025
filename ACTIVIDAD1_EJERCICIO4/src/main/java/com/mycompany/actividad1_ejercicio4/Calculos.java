public class Calculos {
      static double caledad_alberto(double edadjuan){
        double edalberto =2*(edadjuan/3);
        return edalberto;
    }
    static double caledad_ana(double edadjuan){
        double edana =4*(edadjuan/3);
        return edana;
    }
    static double caledad_mama(double edadjuan,double edana,double edalberto){
        double edadmama =edadjuan + edana + edalberto;
        return edadmama;
    }
}
