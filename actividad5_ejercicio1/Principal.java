    private void btnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadActionPerformed
try {
        String nameNumberString;
        String name;
        long number;

        File file = new File("C:\\Users\\Asus\\Documents\\NetBeansProjects\\ACTIVIDAD5_EJERCICIO1\\friendsContact.txt");

        if (!file.exists()) {
            JOptionPane.showMessageDialog(null, "No contacts found (file does not exist).");
            return;
        }

        RandomAccessFile raf = new RandomAccessFile(file, "r");

        StringBuilder data = new StringBuilder();

        while (raf.getFilePointer() < raf.length()) {
            nameNumberString = raf.readLine();

            if (nameNumberString == null || nameNumberString.trim().isEmpty()) {
                continue;
            }

            String[] lineSplit = nameNumberString.split("!");
            if (lineSplit.length < 2) continue;

            name = lineSplit[0];
            number = Long.parseLong(lineSplit[1]);

            data.append("Friend Name: ").append(name).append("\n")
                .append("Contact Number: ").append(number).append("\n\n");
        }

        raf.close();

        if (data.length() == 0) {
            JOptionPane.showMessageDialog(null, "No contacts to display.");
        } else {
            JOptionPane.showMessageDialog(null, data.toString());
        }

    } catch (IOException ioe) {
        ioe.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error reading the file.");
    } catch (NumberFormatException nef) {
        nef.printStackTrace();
        JOptionPane.showMessageDialog(null, "Invalid number format in file.");
    }








    }//GEN-LAST:event_btnReadActionPerformed

