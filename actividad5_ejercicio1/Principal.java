    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
                                         
    try {
        String newName = txtNombre.getText();
        long newNumber = Long.parseLong(txtNumero.getText());

        String nameNumberString;
        String name;
        long number;
        int index;

        File file = new File("C:\\Users\\Asus\\Documents\\NetBeansProjects\\ACTIVIDAD5_EJERCICIO1\\friendsContact.txt");

        if (!file.exists()) {
            file.createNewFile();
        }

        RandomAccessFile raf = new RandomAccessFile(file, "rw");
        boolean found = false;

        // Buscar si existe
        while (raf.getFilePointer() < raf.length()) {
            nameNumberString = raf.readLine();
            String[] lineSplit = nameNumberString.split("!");
            name = lineSplit[0];
            number = Long.parseLong(lineSplit[1]);

            if (name.equals(newName)) {
                found = true;
                break;
            }
        }

        if (found) {
            File tmpFile = new File("temp.txt");
            RandomAccessFile tmpraf = new RandomAccessFile(tmpFile, "rw");

            raf.seek(0);

            while (raf.getFilePointer() < raf.length()) {
                nameNumberString = raf.readLine();
                index = nameNumberString.indexOf('!');
                name = nameNumberString.substring(0, index);

                if (name.equals(newName)) {
                    nameNumberString = name + "!" + newNumber;
                }

                tmpraf.writeBytes(nameNumberString + System.lineSeparator());
            }

            raf.seek(0);
            tmpraf.seek(0);

            while (tmpraf.getFilePointer() < tmpraf.length()) {
                raf.writeBytes(tmpraf.readLine() + System.lineSeparator());
            }

            raf.setLength(tmpraf.length());

            tmpraf.close();
            raf.close();
            tmpFile.delete();

            JOptionPane.showMessageDialog(null, "Friend updated.");
        } else {
            raf.close();
            JOptionPane.showMessageDialog(null, "The name does not exist.");
        }

    } catch (IOException ioe) {
        ioe.printStackTrace();
        JOptionPane.showMessageDialog(null, "File error.");
    } catch (NumberFormatException nef) {
        nef.printStackTrace();
        JOptionPane.showMessageDialog(null, "Invalid number format.");
    }

            
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
        String deleteName = txtNombre.getText().trim();

        if (deleteName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a name to delete.");
            return;
        }

        String nameNumberString;
        String name;
        long number;
        int index;

        File file = new File("C:\\Users\\Asus\\Documents\\NetBeansProjects\\ACTIVIDAD5_EJERCICIO1\\friendsContact.txt");

        if (!file.exists()) {
            JOptionPane.showMessageDialog(null, "File does not exist.");
            return;
        }

        RandomAccessFile raf = new RandomAccessFile(file, "rw");
        boolean found = false;

        // Buscar si el contacto existe
        while (raf.getFilePointer() < raf.length()) {
            nameNumberString = raf.readLine();
            String[] parts = nameNumberString.split("!");
            name = parts[0];
            number = Long.parseLong(parts[1]);

            if (name.equals(deleteName)) {
                found = true;
                break;
            }
        }

        if (found) {

            File tmpFile = new File("temp.txt");
            RandomAccessFile tmpraf = new RandomAccessFile(tmpFile, "rw");

            // Volver al inicio del archivo
            raf.seek(0);

            while (raf.getFilePointer() < raf.length()) {
                nameNumberString = raf.readLine();
                index = nameNumberString.indexOf('!');
                name = nameNumberString.substring(0, index);

                // Si es el contacto a eliminar, NO se copia
                if (name.equals(deleteName)) {
                    continue;
                }

                // Copiar contacto que no fue eliminado
                tmpraf.writeBytes(nameNumberString + System.lineSeparator());
            }

            // Copiar del temporal al original
            raf.seek(0);
            tmpraf.seek(0);

            while (tmpraf.getFilePointer() < tmpraf.length()) {
                raf.writeBytes(tmpraf.readLine() + System.lineSeparator());
            }

            // Ajustar tamaÃ±o del archivo original
            raf.setLength(tmpraf.length());

            tmpraf.close();
            raf.close();
            tmpFile.delete();

            JOptionPane.showMessageDialog(null, "Friend deleted.");
            txtNombre.setText("");
            txtNumero.setText("");

        } else {
            raf.close();
            JOptionPane.showMessageDialog(null, "Name does not exist.");
        }

    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "An error occurred.");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed



